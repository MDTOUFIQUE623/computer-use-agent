# ---------------------------------------------------------------------------
# Phase 3 — CDP Browser Attach
# ---------------------------------------------------------------------------
#
# BROWSER_MODE controls whether the agent spins up its own managed browser
# or attaches to your already-running Chrome via the Chrome DevTools Protocol.
#
#   "launch"  (default) — Playwright launches a fresh Chromium instance for
#             every task. No real cookies/sessions. Safe sandbox. This is the
#             existing behavior up through Phase 2.5d.
#
#   "attach"  — Playwright connects to an already-running Chrome (or any
#             Chromium-based browser) over CDP at CDP_URL. Your real
#             cookies, sessions, and logged-in state are available.
#             Cleanup: close() only disconnects Playwright; it never kills
#             your real browser process.
#
# To use attach mode:
#   1. Start Chrome with the debug port open (see scripts/launch_chrome_cdp.bat
#      or scripts/launch_chrome_cdp.sh — generated by this phase).
#   2. Set BROWSER_MODE=attach in your .env  (or export it in your shell).
#   3. Run the agent normally.  It will automatically use your live session.
#
# CDP_URL defaults to http://localhost:9222 which matches Chrome's default
# --remote-debugging-port. Override in .env if you use a different port.
#
BROWSER_MODE: str = os.getenv("BROWSER_MODE", "launch")   # "launch" | "attach"
CDP_URL:      str = os.getenv("CDP_URL",      "http://localhost:9222")

# How long (ms) to wait for an existing tab to be found / page to settle
# when attaching to a running browser. Only used in attach mode.
CDP_ATTACH_TIMEOUT_MS: int = int(os.getenv("CDP_ATTACH_TIMEOUT_MS", "10000"))

# In attach mode, should we reuse the currently-active tab or always open
# a new one?  "reuse" keeps your browsing session intact; "new" is cleaner
# for automation but opens a tab the user will see afterwards.
CDP_TAB_POLICY: str = os.getenv("CDP_TAB_POLICY", "new")  # "new" | "reuse"


